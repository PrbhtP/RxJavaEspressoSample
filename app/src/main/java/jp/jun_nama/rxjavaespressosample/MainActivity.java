package jp.jun_nama.rxjavaespressosample;

import android.content.Intent;
import android.databinding.DataBindingUtil;
import android.databinding.ViewDataBinding;
import android.support.v7.app.AppCompatActivity;
import android.os.Bundle;
import android.util.Log;
import android.view.View;

import jp.jun_nama.rxjavaespressosample.databinding.ActivityMainBinding;

public class MainActivity extends AppCompatActivity {

    private static final String TAG = MainActivity.class.getSimpleName();

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        ActivityMainBinding binding = DataBindingUtil.setContentView(this, R.layout.activity_main);
        binding.setMainActivity(this);
    }

    public void startRxJava1Activity(View view) {
        Intent i = new Intent(this, RxJava1Activity.class);
        startActivity(i);
    }

    public void startRxJava2Activity(View view) {
        Intent i = new Intent(this, RxJava2Activity.class);
        startActivity(i);
    }
}
